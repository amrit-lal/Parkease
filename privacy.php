  <script>
    // Animate the privacy container when page loads
    document.addEventListener('DOMContentLoaded', function() {
      const privacyContainer = document.getElementById('privacyContainer');
      setTimeout(() => {
        privacyContainer.classList.add('animated');
      }, 100);
      
      // Add scroll animations to sections
      const sections = document.querySelectorAll('.privacy-content h3, .privacy-content h4, .highlight-box');
      
      const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('animate__animated', 'animate__fadeInUp');
            observer.unobserve(entry.target);
          }
        });
      }, {
        threshold: 0.1
      });
      
      sections.forEach(section => {
        observer.observe(section);
      });
    });
  </script>
